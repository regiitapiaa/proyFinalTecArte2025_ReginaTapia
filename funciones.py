def a_rectangulo(x,y):
        area=int(x*y)
        return area

def p_rectangulo(x,y):
        perimetro=int((x*2)+(y*2))
        return perimetro

def a_triangulo(x,y):
        area=float((x*y)/2)
        return area

def p_triangulo(x):
        perimetro=int(x*3)
        return perimetro

def a_circulo(x): 
        area=float(3.1416*(x**2))
        return area

def p_circulo(x):
        perimetro=float(3.1416*(x*2))
        return perimetro
